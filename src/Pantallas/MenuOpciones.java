    private void jButton10ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton10ActionPerformed
        // TODO add your handling code here:
        if(ComboBoxFactura.getSelectedIndex()==0)
        {
            JOptionPane.showMessageDialog(null, "No selecciono una factura para la compra");
        }
        else
        {
            Connection cn=x.getConnection();
            try
            {
                Statement st=cn.createStatement();
                ResultSet rs;
                int libro=0;
                String distribuidor = null;
                
                rs=st.executeQuery("select * from factura where folio_factura="+ComboBoxFactura.getSelectedItem()+"");
                if(rs.next())
                {
                    libro=Integer.parseInt(rs.getString(3));
                    distribuidor=rs.getString(5);
                }
                rs=st.executeQuery("select titutlo from libro where numero_serie="+libro+"");
                if(rs.next())
                {
                    txtLibroCompra.setText(rs.getString(1));
                }
                rs=st.executeQuery("select nombre_distribuidor from distribuidor where rut_distribuidor='"+distribuidor+"'");
                if(rs.next())
                {
                    txtDistribuidorCompra.setText(rs.getString(1));
                }
            }
            catch(SQLException ex)
            {
                JOptionPane.showMessageDialog(null, "Error SQL");
                System.out.println(ex.getMessage());
            }
        }
    }//GEN-LAST:event_jButton10ActionPerformed

