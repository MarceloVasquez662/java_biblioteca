    private void btnBuscarDatosActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBuscarDatosActionPerformed
        // TODO add your handling code here:
        if(boxBoletaVenta.getSelectedIndex()==0)
        {
            JOptionPane.showMessageDialog(null, "No selecciono una boleta para la venta");
        }
        else
        {
            Connection cn=x.getConnection();
            try
            {
                Statement st=cn.createStatement();
                ResultSet rs;
                int libro=0;
                String cliente = null, trabajador = null;
                
                rs=st.executeQuery("select * from boleta where folio_boleta="+boxBoletaVenta.getSelectedItem()+"");
                if(rs.next())
                {
                    libro=Integer.parseInt(rs.getString(4));
                    cliente=rs.getString(6);
                    trabajador=rs.getString(5);
                }
                rs=st.executeQuery("select titutlo from libro where numero_serie="+libro+"");
                if(rs.next())
                {
                    txtLibroVenta.setText(rs.getString(1));
                }
                rs=st.executeQuery("select nombre_persona, apellido_paterno, apellido_materno from persona where Cliente_rut_cliente='"+cliente+"'");
                if(rs.next())
                {
                    txtClienteVenta.setText(rs.getString(1)+" "+rs.getString(2)+" "+rs.getString(3));
                }
                rs=st.executeQuery("select nombre_persona, apellido_paterno, apellido_materno from persona where Trabajador_rut_trabajador='"+trabajador+"'");
                if(rs.next())
                {
                    txtTrabajadorVenta.setText(rs.getString(1)+" "+rs.getString(2)+" "+rs.getString(3));
                }
            }
            catch(SQLException ex)
            {
                JOptionPane.showMessageDialog(null, "Error SQL");
                System.out.println(ex.getMessage());
            }
        }
    }//GEN-LAST:event_btnBuscarDatosActionPerformed

