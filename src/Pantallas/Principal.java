    private void btnIngresarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnIngresarActionPerformed
        // TODO add your handling code here:
        String usuario, contra;
        boolean sw=true;
        
        usuario=txtUsuario.getText();
        contra=txtContra.getText();
        
        if(usuario.equals("") || contra.equals(""))
        {
            JOptionPane.showMessageDialog(null, "Algunos campos estan vacios.");
            sw=false;
        }
        
        if(sw==true)
        {
            x=new conexion();
            Connection cn=x.getConnection();
            try 
            {
                Statement st=cn.createStatement();
                ResultSet rs;
                rs=st.executeQuery("select rut_trabajador, contrase単a from mydb.trabajador where rut_trabajador='"+usuario+"' and contrase単a='"+contra+"' ");
                if(rs.next())
                {
                    MenuOpciones x=new MenuOpciones();
                    x.setVisible(true);
                    dispose();
                }
                else
                {
                    JOptionPane.showMessageDialog(null, "Usuario o Contrase単a son incorrectos.");
                }
            } 
            catch (SQLException ex) 
            {
                Logger.getLogger(Principal.class.getName()).log(Level.SEVERE, null, ex);
            }
            
        }
        
        
    }//GEN-LAST:event_btnIngresarActionPerformed

