    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        int folio=0, precioNeto=0, precioBruto, iva;
        String fecha="", id;
        int  dia, mes, anio, idLibro;
        
        boolean sw=true;
        
        
        try
        {
            folio=Integer.parseInt(txtFolio.getText());
        }
        catch(NumberFormatException ex)
        {
            JOptionPane.showMessageDialog(null, "Folio ingresado no es numerico entero.");
            sw=false;
        }
        
        try
        {
            precioNeto=Integer.parseInt(txtNeto.getText());
        }
        catch(NumberFormatException ex)
        {
            JOptionPane.showMessageDialog(null, "Precio neto ingresado no es numerico entero.");
            sw=false;
        }
        
        try
        {
            dia=calendarioFactura.getCalendar().get(Calendar.DAY_OF_MONTH);
            mes=calendarioFactura.getCalendar().get(Calendar.MONTH)+1;
            anio=calendarioFactura.getCalendar().get(Calendar.YEAR);
            fecha=(dia+"-"+mes+"-"+anio);
        }
        catch(NullPointerException ex)
        {
            JOptionPane.showMessageDialog(null, "Fecha no valida");
            sw=false;
        }
        
        if(ComboBoxLibro.getSelectedIndex()==0)
        {
            JOptionPane.showMessageDialog(null, "No selecciono un libro para la factura");
            sw=false;
        }
        
        if(ComboBoxMetodoPago.getSelectedIndex()==0)
        {
            JOptionPane.showMessageDialog(null, "No selecciono un metodo de pago");
            sw=false;
        }
        if(ComboBoxDistribuidor.getSelectedIndex()==0)
        {
            JOptionPane.showMessageDialog(null, "No se selecciono un distribuidor.");
            sw=false;
        }
        
        if(sw==true)
        {
            id=String.valueOf(ComboBoxLibro.getSelectedItem());
            idLibro=Integer.parseInt(id);
            Connection cn=x.getConnection();
            try 
            {
                Statement st=cn.createStatement();
                ResultSet rs;
                int i;
                
                i=st.executeUpdate("update mydb.factura set fecha_compra='"+fecha+"', Metodo_pago='"+ComboBoxMetodoPago.getSelectedItem()+"', Distribuidor='"+ComboBoxDistribuidor.getSelectedItem()+"' where folio_factura="+folio+"");
                
                if(i!=0)
                {
                    iva=precioNeto*19/100;
                    precioBruto=precioNeto+iva;
                    
                    st.executeUpdate("update mydb.documento set precio_bruto="+precioBruto+", precio_neto="+precioNeto+", iva="+iva+" where Factura_folio_factura="+folio+"");
                    
                    JOptionPane.showMessageDialog(null, "Factura actualizada con exito");
                    MenuOpciones x=new MenuOpciones();
                    x.setVisible(true);
                    dispose();
                }
            } 
            catch (SQLException ex) 
            {
                JOptionPane.showMessageDialog(null, "Factura que intenta ingresar ya se encuentra en la BD");
                System.out.println(ex.getMessage());
            }
        }
        
    }//GEN-LAST:event_jButton1ActionPerformed

    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        // TODO add your handling code here:
        String rutDistribuidor;
        if(ComboBoxDistribuidor.getSelectedIndex()==0)
        {
                JOptionPane.showMessageDialog(null, "No escogio un distribuidor");
        }
        else
        {
            rutDistribuidor=(String)ComboBoxDistribuidor.getSelectedItem();
            try
            {
                Connection cn=x.getConnection();
                Statement st=cn.createStatement();
                ResultSet rs;
                rs=st.executeQuery("select nombre_distribuidor from distribuidor where rut_distribuidor='"+rutDistribuidor+"'");
                if(rs.next())
                {
                    txtDistribuidor.setText(rs.getString(1));
                }
            }
            catch(SQLException ex)
            {
                System.out.println(ex.getMessage());
            }
        }
    }//GEN-LAST:event_jButton3ActionPerformed

