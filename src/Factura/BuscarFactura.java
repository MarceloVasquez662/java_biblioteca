    private void setFilas()
    {
        try
        {
            Connection cn=x.getConnection();
            Statement st=cn.createStatement();
            ResultSet rs;
            rs=st.executeQuery("select factura.folio_factura, documento.precio_neto, documento.iva, documento.precio_bruto, libro.titutlo, factura.Distribuidor, factura.Metodo_Pago, factura.fecha_compra from libro join factura on factura.Libro_numero_serie=libro.numero_serie join documento on documento.Factura_folio_factura=factura.folio_factura");
            
            Object datos[]=new Object[8];
            
            while(rs.next())
            {
                for(int i=0; i<8; i++)
                {
                    datos[i]=rs.getObject(i+1);
                }
                modeloBuscarFactura.addRow(datos);
            }
            
        } 
        catch (SQLException ex) 
        {
            Logger.getLogger(EliminarDistribuidor.class.getName()).log(Level.SEVERE, null, ex);
        }
    }
    
