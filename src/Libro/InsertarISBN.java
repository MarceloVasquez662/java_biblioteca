    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        int ISBN;
        
        try
        {
            ISBN=Integer.parseInt(txtISBN.getText());
            
            try
            {
                x=new conexion();
                Connection cn=x.getConnection();
                Statement st=cn.createStatement();
                
                int i;
                i=st.executeUpdate("insert into isbn values ("+ISBN+")");
                if(i!=0)
                {
                    JOptionPane.showMessageDialog(null, "ISBN insertado exitosamente");
                    MenuOpciones x=new MenuOpciones();
                    x.setVisible(true);
                    dispose();
                }
            }
            catch(SQLException ex)
            {
                JOptionPane.showMessageDialog(null, "El isbn que intentar ingresar, ya se encuentra en la BD");
                System.out.println(ex.getMessage());
            }
        }
        catch(NumberFormatException ex)
        {
            JOptionPane.showMessageDialog(null, "ISBN insertado no es numerico entero");
        }
    }//GEN-LAST:event_jButton1ActionPerformed

