        BoxISBN.addItem("ISBN");
        
        ArrayList <String> listaCategoria=new ArrayList <String>();
        ArrayList <String> listaIdioma=new ArrayList <String>();
        ArrayList <String> listaEditorial=new ArrayList <String>();
        ArrayList <String> listaAutor=new ArrayList <String>();
        ArrayList <String> listaISBN=new ArrayList <String>();
        listaAutor=llenarListaAutor();
        listaCategoria=llenarListaCategoria();
        listaEditorial=llenarListaEditorial();
        listaIdioma=llenarListaIdioma();
        listaISBN=llenarListaISBN();
        
        for(int i=0; i<listaAutor.size(); i++)
        {
            BoxAutor.addItem(listaAutor.get(i));
        }
        for(int i=0; i<listaCategoria.size(); i++)
        {
            BoxCategoria.addItem(listaCategoria.get(i));
        }
        for(int i=0; i<listaEditorial.size(); i++)
        {
            BoxEditorial.addItem(listaEditorial.get(i));
        }
        for(int i=0; i<listaIdioma.size(); i++)
        {
            BoxIdioma.addItem(listaIdioma.get(i));
        }
        for(int i=0; i<listaISBN.size(); i++)
        {
            BoxISBN.addItem(listaISBN.get(i));
        }
