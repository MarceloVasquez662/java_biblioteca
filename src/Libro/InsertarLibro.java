    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        String nombre, estado ="Disponible";
        int codigo = 0, paginas = 0, precio = 0, anio = 0;
        boolean sw=true;
        
        nombre=txtNombre.getText();
        
        if(nombre.equals(""))
        {
            JOptionPane.showMessageDialog(null, "Campo nombre esta vacio");
            sw=false;
        }
        
        try
        {
            codigo=Integer.parseInt(txtCodigo.getText());
        }
        catch(NumberFormatException ex)
        {
            JOptionPane.showMessageDialog(null, "Codigo ingresado no es numerico entero");
            sw=false;
        }
        
        try
        {
            precio=Integer.parseInt(txtPrecio.getText());
        }
        catch(NumberFormatException ex)
        {
            JOptionPane.showMessageDialog(null, "Precio ingresado no es numerico entero");
            sw=false;
        }
        
        try
        {
            paginas=Integer.parseInt(txtPaginas.getText());
        }
        catch(NumberFormatException ex)
        {
            JOptionPane.showMessageDialog(null, "Paginas ingresadas no son numericos enteros");
            sw=false;
        }
        
        try
        {
            anio=calendarioLibro.getYear();
        }
        catch(NumberFormatException ex)
        {
            JOptionPane.showMessageDialog(null, "Año ingresado no es valido");
            sw=false;
        }
        
        
        if(BoxAutor.getSelectedIndex()==0 || BoxCategoria.getSelectedIndex()==0 || BoxEditorial.getSelectedIndex()==0 || BoxIdioma.getSelectedIndex()==0 || BoxISBN.getSelectedIndex()==0)
        {
            sw=false;
            JOptionPane.showMessageDialog(null, "No selecciono autor, categoria, editorial, idioma o ISBN");
        }
        
        
        if(sw==true)
        {
            x=new conexion();
            
            try
            {
                Connection cn=x.getConnection();
                Statement st=cn.createStatement();
                ResultSet rs;
                int i;
                i=st.executeUpdate("insert into mydb.libro values ("+codigo+",'"+nombre+"', "+paginas+", "+precio+", '"+anio+"', '"+estado+"', '"+BoxCategoria.getSelectedItem()+"', '"+BoxIdioma.getSelectedItem()+"', "+BoxISBN.getSelectedItem()+",'"+BoxEditorial.getSelectedItem()+"')");
                
                if(i!=0)
                {
                    rs=st.executeQuery("select rut  from mydb.autor where rut='"+BoxAutor.getSelectedItem()+"'");
                    String rutAutor="";
                    if(rs.next())
                    {
                        rutAutor=rs.getString(1);
                    }
                    
                    i=st.executeUpdate("insert into libro_autor values ('"+rutAutor+"', "+codigo+")"); 
                    JOptionPane.showMessageDialog(null, "Libro ingresado exitosamente");
                    MenuOpciones x= new MenuOpciones();
                    x.setVisible(true);
                    dispose();
                }
            }
            catch(SQLException ex)
            {
                JOptionPane.showMessageDialog(null, "Codigo que intenta ingresar ya se encuentra en la BD");
                System.out.println(ex.getMessage());
            }
        }
    }//GEN-LAST:event_jButton1ActionPerformed

