    private void txtBuscadoKeyTyped(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txtBuscadoKeyTyped
        // TODO add your handling code here:
        if(radioAutor.isSelected() || radioCategoria.isSelected() || radioCodigo.isSelected() || radioEditorial.isSelected() || radioISBN.isSelected() || radioIdioma.isSelected() || radioNombre.isSelected())
        {
            
            
            if(radioAutor.isSelected())
            {
              txtBuscado.addKeyListener(new KeyAdapter() {
                @Override
                public void keyReleased(KeyEvent e) 
                {
                    trs.setRowFilter(RowFilter.regexFilter("(?i)"+txtBuscado.getText(), 10));
                }
                });  
            }
            
            if(radioCategoria.isSelected())
            {
              txtBuscado.addKeyListener(new KeyAdapter() {
                @Override
                public void keyReleased(KeyEvent e) 
                {
                    trs.setRowFilter(RowFilter.regexFilter("(?i)"+txtBuscado.getText(), 6));
                }
                });  
            }
            if(radioCodigo.isSelected())
            {
              txtBuscado.addKeyListener(new KeyAdapter() {
                @Override
                public void keyReleased(KeyEvent e) 
                {
                    trs.setRowFilter(RowFilter.regexFilter("(?i)"+txtBuscado.getText(), 0));
                }
                });  
            }
            
            if(radioEditorial.isSelected())
            {
              txtBuscado.addKeyListener(new KeyAdapter() {
                @Override
                public void keyReleased(KeyEvent e) 
                {
                    trs.setRowFilter(RowFilter.regexFilter("(?i)"+txtBuscado.getText(), 9));
                }
                });  
            }
            
            if(radioISBN.isSelected())
            {
              txtBuscado.addKeyListener(new KeyAdapter() {
                @Override
                public void keyReleased(KeyEvent e) 
                {
                    trs.setRowFilter(RowFilter.regexFilter("(?i)"+txtBuscado.getText(), 8));
                }
                });  
            }
            
            if(radioIdioma.isSelected())
            {
              txtBuscado.addKeyListener(new KeyAdapter() {
                @Override
                public void keyReleased(KeyEvent e) 
                {
                    trs.setRowFilter(RowFilter.regexFilter("(?i)"+txtBuscado.getText(), 7));
                }
                });  
            }
            
            if(radioNombre.isSelected())
            {
              txtBuscado.addKeyListener(new KeyAdapter() {
                @Override
                public void keyReleased(KeyEvent e) 
                {
                    trs.setRowFilter(RowFilter.regexFilter("(?i)"+txtBuscado.getText(), 1));
                }
                });  
            }
            
            trs=new TableRowSorter(modeloBuscarLibro);
            jTable1.setRowSorter(trs);
        }
        else
        {
            JOptionPane.showMessageDialog(null, "No selecciono porque opcion filtrar");
        }
    }//GEN-LAST:event_txtBuscadoKeyTyped

