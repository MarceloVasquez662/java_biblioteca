    private void btnVerActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnVerActionPerformed
        // TODO add your handling code here:
        if(BoxLibro.getSelectedIndex()==0)
        {
            JOptionPane.showMessageDialog(null, "No selecciono un libro");
        }
        else
        {
            try
            {
                Connection cn=x.getConnection();
                Statement st=cn.createStatement();
                ResultSet rs;
                rs=st.executeQuery("select titutlo from libro where numero_serie="+BoxLibro.getSelectedItem()+"");
                if(rs.next())
                {
                    txtLibro.setText(rs.getString(1));
                }
                
            }
            catch(SQLException ex)
            {
                System.out.println(ex.getMessage());
            }
        }
    }//GEN-LAST:event_btnVerActionPerformed

