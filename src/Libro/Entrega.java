    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        // TODO add your handling code here:
        int diasRetraso,multa, total;
        
        multa=Integer.parseInt(txtMulta.getText());
        try
        {
            diasRetraso=Integer.parseInt(txtRetraso.getText());
            total=multa*diasRetraso;
            txtCostoTotal.setText(String.valueOf(total));
        }
        catch(NumberFormatException ex)
        {
            JOptionPane.showMessageDialog(null, "Dias de retraso no es numerico entero");
        }
    }//GEN-LAST:event_jButton2ActionPerformed

    private void jButton11ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton11ActionPerformed
        // TODO add your handling code here:
        int dia, mes, anio, diasRetraso = 0, total;
        String fechaEntrega="";
        boolean sw=true;
        
        if(BoxLibro.getSelectedIndex()==0)
        {
            JOptionPane.showMessageDialog(null, "No selecciono un libro para la entrega");
            sw=false;
        }
        
        try
        {
            dia=calendarioEntrega.getCalendar().get(Calendar.DAY_OF_MONTH);
            mes=calendarioEntrega.getCalendar().get(Calendar.MONTH)+1;
            anio=calendarioEntrega.getCalendar().get(Calendar.YEAR);
            fechaEntrega=(dia+"-"+mes+"-"+anio);
        }
        catch(NullPointerException ex)
        {
            JOptionPane.showMessageDialog(null, "La fecha ingresada no es valida");
            sw=false;
        }
        
        try
        {
            diasRetraso=Integer.parseInt(txtRetraso.getText());
        }
        catch(NumberFormatException ex)
        {
            JOptionPane.showMessageDialog(null, "Dias de retraso no es numerico entero");
            sw=false;
        }
        
        if(txtCostoTotal.getText().equals(""))
        {
            JOptionPane.showMessageDialog(null, "Presione el boton confirmar retraso para continuar");
            sw=false;
        }
        
        if(sw==true)
        {
            try
            {
                Connection cn=x.getConnection();
                Statement st=cn.createStatement();
                int i;
                
                i=st.executeUpdate("update arriendo set costo_atraso="+txtCostoTotal.getText()+", fecha_entrega='"+fechaEntrega+"', dias_atraso="+diasRetraso+" where Libro_numero_serie="+BoxLibro.getSelectedItem()+"");
                if(i!=0)
                {
                    JOptionPane.showMessageDialog(null, "Libro entregado, ya se encuentra disponible");
                    st.executeUpdate("update libro set estado='Disponible' where numero_serie="+BoxLibro.getSelectedItem()+"");
                    MenuOpciones x=new MenuOpciones();
                    x.setVisible(true);
                    dispose();
                }
            }
            catch(SQLException ex)
            {
                System.out.println(ex.getMessage());
            }
        }
    }//GEN-LAST:event_jButton11ActionPerformed

