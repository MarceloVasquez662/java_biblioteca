    private void setFilas()
    {
        try
        {
            Connection cn=x.getConnection();
            Statement st=cn.createStatement();
            ResultSet rs, rs2;
            rs=st.executeQuery("select numero_serie, titutlo, numero_paginas, precio_referencial, anio_publicacion, estado, Categoria_nombre_categoria, Idioma_nombre_idioma, ISBN_id_isbn, Editorial_nombre_editorial, persona.Autor_rut from libro join libro_autor on libro.numero_serie=libro_autor.Libro_numero_serie join autor on autor.rut=libro_autor.Autor_rut join persona on persona.Autor_rut=autor.rut where libro.Estado='Disponible'");
            
            Object datos[]=new Object[11];
            
            while(rs.next())
            {
                for(int i=0; i<11; i++)
                {
                    datos[i]=rs.getObject(i+1);
                }
                modeloEliminarLibro.addRow(datos);
            }
            
        } 
        catch (SQLException ex) 
        {
            Logger.getLogger(EliminarDistribuidor.class.getName()).log(Level.SEVERE, null, ex);
        }
    }
    
