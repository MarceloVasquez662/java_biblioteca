    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        int codigo=0;
        boolean sw=true;
        
        try
        {
            codigo=Integer.parseInt(txtCodigo.getText());
        }
        catch(NumberFormatException ex)
        {
            JOptionPane.showMessageDialog(null, "Codigo ingresado no es numerico entero");
            sw=false;
        }
        
        if(sw==true)
        {
            try
            {
                Connection cn=x.getConnection();
                Statement st=cn.createStatement();
                ResultSet rs;
                int i=0;
                
                rs=st.executeQuery("select * from venta where Libro_numero_serie="+codigo+"");
                if(rs.next())
                {
                    i=1;
                }
                rs=st.executeQuery("select * from arriendo where Libro_numero_serie="+codigo+"");
                if(rs.next())
                {
                    i=1;
                }
                rs=st.executeQuery("select * from factura where Libro_numero_serie="+codigo+"");
                if(rs.next())
                {
                    i=1;
                }
                rs=st.executeQuery("select * from boleta where Libro_numero_serie="+codigo+"");
                if(rs.next())
                {
                    i=1;
                }
                rs=st.executeQuery("select * from compra where Libro_numero_serie="+codigo+"");
                if(rs.next())
                {
                    i=1;
                }
                
                
                if(i==0)
                {
                    st.executeUpdate("delete from libro_autor where Libro_numero_serie="+codigo+"");
                    st.executeUpdate("delete from libro_distribuidor where Libro_numero_serie="+codigo+"");
                    i=st.executeUpdate("delete from libro where numero_serie="+codigo+"");
                    if(i!=0)
                    {
                        JOptionPane.showMessageDialog(null, "Libro eliminado exitosamente");
                        MenuOpciones x=new MenuOpciones();
                        x.setVisible(true);
                        dispose();
                    }
                    else
                    {
                        JOptionPane.showMessageDialog(null, "El libro no se encuentra en la BD");
                    }
                }
                else
                {
                    JOptionPane.showMessageDialog(null, "El libro que desea eliminar esta bloqueado por clave foranea");
                }
            }
            catch(SQLException ex)
            {
                System.out.println(ex.getMessage());
            }
        }
    }//GEN-LAST:event_jButton1ActionPerformed

