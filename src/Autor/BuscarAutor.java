    private void setFilas()
    {
        try
        {
            Connection cn=x.getConnection();
            Statement st=cn.createStatement();
            ResultSet rs;
            rs=st.executeQuery("select autor.rut, persona.nombre_persona, persona.apellido_paterno, persona.apellido_materno from autor join persona on autor.rut=persona.Autor_rut");
            
            Object datos[]=new Object[4];
            
            while(rs.next())
            {
                for(int i=0; i<4; i++)
                {
                    datos[i]=rs.getObject(i+1);
                }
                modeloBuscarAutor.addRow(datos);
            }
            
        } 
        catch (SQLException ex) 
        {
            Logger.getLogger(EliminarDistribuidor.class.getName()).log(Level.SEVERE, null, ex);
        }
    }
    
