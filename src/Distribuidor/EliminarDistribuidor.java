    private void setFilas()
    {
        try
        {
            Connection cn=x.getConnection();
            Statement st=cn.createStatement();
            ResultSet rs;
            rs=st.executeQuery("select rut_distribuidor, nombre_distribuidor, direccion, telefono, anio_vendiendo from mydb.distribuidor");
            
            Object datos[]=new Object[5];
            
            while(rs.next())
            {
                for(int i=0; i<5; i++)
                {
                    datos[i]=rs.getObject(i+1);
                }
                modeloTablaDistribuidor.addRow(datos);
            }
            
        } 
        catch (SQLException ex) 
        {
            Logger.getLogger(EliminarDistribuidor.class.getName()).log(Level.SEVERE, null, ex);
        }
    }
    
