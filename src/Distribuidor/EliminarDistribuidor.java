    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        
        String rut;
        rut=txtRut.getText();
        
        if(rut.equals(""))
        {
            JOptionPane.showMessageDialog(null, "Campo de rut a eliminar esta vacio");
        }
        else
        {
            Connection cn=x.getConnection();
            try 
            {
                Statement st=cn.createStatement();
                int i;
                i=st.executeUpdate("delete from mydb.distribuidor where rut_distribuidor='"+rut+"'");
                
                if(i!=0)
                {
                    JOptionPane.showMessageDialog(null, "Distribuidor eliminado");
                    MenuOpciones x=new MenuOpciones();
                    x.setVisible(true);
                    dispose();
                }
                else
                {
                    JOptionPane.showMessageDialog(null, "No se encontro el distribuidor que desea eliminar");
                }
            } 
            catch (SQLException ex) 
            {
                JOptionPane.showMessageDialog(null, "El trabajador que desea eliminar esta bloqueado por clave foranea");
            }
        }
    }//GEN-LAST:event_jButton1ActionPerformed

