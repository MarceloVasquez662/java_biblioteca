    private void btnBuscarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBuscarActionPerformed
        // TODO add your handling code here:
        String idioma=txtIdioma.getText();
        
        if(idioma.equals(""))
        {
            JOptionPane.showMessageDialog(null, "Campo de texto idioma esta vacio");
        }
        else
        {
            try
            {
                x=new conexion();
                Connection cn=x.getConnection();
                Statement st=cn.createStatement();
                ResultSet rs;
                rs=st.executeQuery("select * from mydb.idioma where nombre_idioma='"+idioma+"'");
                
                if(rs.next())
                {
                    MostrarModificar x=new MostrarModificar();
                    x.txtNombreModificar.setText(idioma);
                    x.setVisible(true);
                    dispose();
                }
                else
                {
                    JOptionPane.showMessageDialog(null, "El idioma que ingreso no se encuentra en la BD");
                }
            }
            catch(SQLException ex)
            {
                JOptionPane.showMessageDialog(null, "Error SQL");
            }
        }
    }//GEN-LAST:event_btnBuscarActionPerformed

