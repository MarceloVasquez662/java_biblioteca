    private void btnEliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEliminarActionPerformed
        // TODO add your handling code here:
        String nombre=txtEditorial.getText();
        if(nombre.equals(""))
        {
            JOptionPane.showMessageDialog(null, "Campo de texto esta vacio");
        }
        else
        {
            Connection cn=x.getConnection();
            try 
            {
                Statement st=cn.createStatement();
                int i;
                i=st.executeUpdate("delete from mydb.editorial where nombre_editorial='"+nombre+"'");
                
                if(i!=0)
                {
                    JOptionPane.showMessageDialog(null, "Editorial eliminada");
                    OpcionesEditorial x=new OpcionesEditorial();
                    x.setVisible(true);
                    dispose();
                }
                else
                {
                    JOptionPane.showMessageDialog(null, "No se encontro la editorial que desea eliminar");
                }
            } 
            catch (SQLException ex) 
            {
                JOptionPane.showMessageDialog(null, "La editorial que quiere eliminar esta bloqueada por clave foranea");
            }
        }
    }//GEN-LAST:event_btnEliminarActionPerformed

