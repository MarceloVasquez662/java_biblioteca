    private void btnAgregarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAgregarActionPerformed
        // TODO add your handling code here:
        String correo=txtCorreo.getText();
        int id=0;
        boolean sw=true;
        
        try
        {
            id=Integer.parseInt(txtIDCorreo.getText());
        }
        catch(NumberFormatException ex)
        {
            JOptionPane.showMessageDialog(null, "El id ingresado debe ser numerico entero");
            sw=false;
        }
        
        if(correo.equals(""))
        {
            sw=false;
            JOptionPane.showMessageDialog(null, "Campo vacio en el cuadro correo");
        }
        
        if(sw==true)
        {
            x=new conexion();
            Connection cn=x.getConnection();
            try 
            {
                Statement st=cn.createStatement();
                int i;
                i=st.executeUpdate("insert into mydb.correos values ('"+correo+"', "+id+")");
                if(i!=0)
                {
                    JOptionPane.showMessageDialog(null, "Correo agregado con exito");
                     this.setVisible(false);
                                   }
            } 
            catch (SQLException ex) 
            {
                JOptionPane.showMessageDialog(null, "El id ingresado ya se encuentra en la BD");
            }
        }
    }//GEN-LAST:event_btnAgregarActionPerformed

