    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        String rut="", nombre, ApPaterno, ApMaterno;
        int rutL;
        
        nombre=txtNombre.getText();
        ApPaterno=txtApPaterno.getText();
        ApMaterno=txtApMaterno.getText();
        boolean sw=true;
        
        try
        {
            rutL=Integer.parseInt(txtRut.getText());
            rut=Integer.toString(rutL);
        }
        catch(NumberFormatException ex)
        {
            JOptionPane.showMessageDialog(null, "Rut ingresado es alfanumerico o esta vacio. Solo ingrese el rut sin punto ni guion");
            sw=false;
        }
        
        if(nombre.equals("") || ApPaterno.equals("") || ApMaterno.equals(""))
        {
            JOptionPane.showMessageDialog(null, "Campos vacios, verifique lo ingresado");
            sw=false;
        }
        if(rut.length()<8 || rut.length()>9)
        {
            JOptionPane.showMessageDialog(null, "El rut ingresado no cumple con el tama√±o requerido (8 a 9)");
            sw=false;
        }
        
        if(sw==true)
        {
            try
            {
               x=new conexion();
               Connection cn=x.getConnection();
               Statement st=cn.createStatement();
               
               int i, m;
               i=st.executeUpdate("insert into mydb.autor values ('"+rut+"')");
               m=st.executeUpdate("insert into mydb.persona values ('"+nombre+"', '"+ApPaterno+"', '"+ApMaterno+"', null, null, '"+rut+"')");
               
               if(i!=0 && m!=0)
               {
                   JOptionPane.showMessageDialog(null, "Autor ingresado con exito!");
                   this.setVisible(false);
               }
               
            }
            catch(SQLException ex)
            {
                JOptionPane.showMessageDialog(null, "El rut ingresado ya se encuentra en la BD");
            }
        }
        
        
    }//GEN-LAST:event_jButton1ActionPerformed

